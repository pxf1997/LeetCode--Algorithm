    class Solution {
        public int climbStairs1(int n) {
            if (n <= 2) return n;
            int[] dp = new int[n + 1];
            int len = dp.length;
            dp[0] = 0;
            dp[1] = 1;
            dp[2] = 2;
            for (int i = 3; i < len; i++) {
                dp[i] = dp[i - 1] + dp[i - 2];
            }
            return dp[n];
        }

//        æ»šåŠ¨æ•°ç»„
        public int climbStairs(int n) {
            int p = 0, q = 0, r = 1;
            for (int i = 1; i <= n; ++i) {
                p = q;
                q = r;
                r = p + q;
            }
            return r;
        }
    }
